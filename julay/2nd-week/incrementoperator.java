public class incrementoperator {
   public static void main(String[] args) {
    int numberOne = 1;

    numberOne += 1;
    System.out.println(numberOne);

    numberOne -= 1;
    System.out.println(numberOne);

    numberOne /= 1;
    System.out.println(numberOne);

    numberOne *= 1;
    System.out.println(numberOne);

    numberOne %= 1;
    System.out.println(numberOne);
  }
}
